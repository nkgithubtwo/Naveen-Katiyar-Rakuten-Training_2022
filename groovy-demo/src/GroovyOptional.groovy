
class GroovyOptional {
	
	static void main(String[] args) {
		def a = 200;
		def b = 100.00;
		def c = 100.02;
		def d = "Hello World !! ..."
		
		println(a)
		println(b)
		println(c)
		println(d)
		
		def aInt = 100;
		def bFloat = 100.00;
		def cDouble = 100.08;
		def dString = "Kanpur"
		
		println(aInt)
		println(bFloat)
		println(cDouble)
		println(dString)
	}
}
